pub const SEQUENCE: [char; 8] = ['m', 'r', 'l', 'y', 'p', 'r', 'o', 'd'];

pub fn strokes(letter: char) -> &'static [&'static [(usize, usize)]] {
    match letter {
        'm' => &[
            &[(4, 0), (3, 0), (2, 0), (1, 0), (0, 0)],
            &[(0, 1), (0, 2), (0, 3), (0, 4)],
            &[(1, 4), (2, 4), (3, 4), (4, 4)],
            &[(1, 2), (2, 2), (3, 2), (4, 2)],
        ],
        'r' => &[
            &[(4, 0), (3, 0), (2, 0), (1, 0), (0, 0)],
            &[(0, 1), (0, 2), (0, 3), (0, 4)],
        ],
        'l' => &[
            &[(0, 0), (1, 0), (2, 0), (3, 0), (4, 0)],
            &[(4, 1), (4, 2), (4, 3), (4, 4)],
        ],
        'y' => &[
            &[(0, 0), (1, 0), (2, 0)],
            &[(2, 1), (2, 2), (2, 3)],
            &[(0, 4), (1, 4), (2, 4)],
            &[(3, 4), (4, 4), (4, 3), (4, 2), (4, 1), (4, 0)],
        ],
        'p' => &[
            &[(4, 0), (3, 0), (2, 0), (1, 0), (0, 0)],
            &[(0, 1), (0, 2), (0, 3), (0, 4)],
            &[(1, 4)],
            &[(2, 4), (2, 3), (2, 2), (2, 1)],
        ],
        'o' => &[
            &[(4, 0), (3, 0), (2, 0), (1, 0), (0, 0)],
            &[(0, 1), (0, 2), (0, 3), (0, 4)],
            &[(1, 4), (2, 4), (3, 4), (4, 4)],
            &[(4, 3), (4, 2), (4, 1)],
        ],
        'd' => &[
            &[(2, 3), (2, 2), (2, 1), (2, 0)],
            &[(3, 0), (4, 0)],
            &[(4, 1), (4, 2), (4, 3), (4, 4)],
            &[(3, 4), (2, 4), (1, 4), (0, 4)],
        ],
        _ => &[],
    }
}

pub fn bits(letter: char) -> [[u8; 5]; 5] {
    let mut grid = [[0u8; 5]; 5];
    for stroke in strokes(letter) {
        for &(r, c) in *stroke {
            grid[r][c] = 1;
        }
    }
    grid
}
